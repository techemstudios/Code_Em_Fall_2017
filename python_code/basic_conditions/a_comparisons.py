##
# Our variables we'll use to compare
a = 10
b = 5

# Checking if less than, greater than
print("""
Checking if less than,
or greater than.
"""
      )

if a < b:
    print("a is less than b.")
if a > b:
    print("a is greater than b.")
    
print("\nDone.")

##
# Checking if less than or equal to, greater than or equal to
print("""
Checking if less than OR equal to,
greater than OR equal to.
"""
      )

if a <= b:
    print("a is less than or equal to b.")
if a >= b:
    print("a is greater than or equal to b.")
    
print("\nDone.")

##
# Checking if equal to, not equal to
print("""
Checking if equal to,
or not equal to.
"""
      )

if a == b:
    print("a is equal to b")
if a != b:
    print("a and b are not equal")

print("\Done.")
