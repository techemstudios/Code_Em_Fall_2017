# STORING DATA

# Notes taken from Python Crash Course

# Many programs will ask for specific information from users.

# User could be ask for preferences concerning a game or provide information
# for visualization. Either way, you'll store the data from users in
# data structures such as lists and dictionaries.

# When users close the program, you'll almost always want to save the inforamtion
# they entered.

# A simple way to do this involves storing your data using the kson module.

# JSON
# allows you to dump simple Python data structures into a file and load the data
# from that file the next time the program runs.
# You can also use json to share data between different Python programs.

# Even better! The JSON format is not specific to Python. This enables you to share
# data you store in JSON format with people who work in other languages besides Python.
