# Fancy Credits
# Demonstrates escape sequences
# the print("\a") produces a sound when you open the program...
# directly from the operating system, not through IDLE. i.e. by double-clicking the actual py file

# Sequence              Description
# \\ Backslash.         Prints one backslash.
# \' Single quote.      Prints a single quote.
# \" Double quote.      Prints a double quote.
# \a Bell.              Sounds the system bell.
# \n Newline.           Moves cursor to beginning of next line.
# \t Horizontal tab.    Moves cursor forward one tab stop.

print("\t\t\tFancy Credits")

print("\t\t\t \\ \\ \\ \\ \\ \\ \\")
print("\t\t\t\tby")
print("\t\t\t\tMayfly")
print("\t\t\t \\ \\ \\ \\ \\ \\ \\")

print("\nSpecial thanks goes out to:")
print("The Odonata, Dragonfly \'This Side of the Rock,\' who never says \"can\'t.\"")

# sound the system bell
print("\a")

raw_input("\n\nPress the enter key to exit.")
